package var;

public class Var_type {

    public  static void main(String[] args) {
    int a = 100; //정수
    double b = 10.5; //실수
    boolean c = true; //true, false 입력 가능
    char d = 'A'; //문자 하나
    String e ="Hello Java"; //문자열, 문자열을 다루기위한 특별한 타입


    //정수
    byte f = 127; //-128~127
    short g = 32767; // -32,768 ~ 32,767
    int h = 2147483647; //-2,147,483,648 ~ 2,147,483,647 (약 20억)

    //-9,223,372,036,854,775,808 ~ 9,223,372,036,854,775,807
     long i = 9223372036775807L;

    //실수
     float j = 10.0f;
     double k = 10.0;
         }
}
